#/n - new line
print("Line 1\nLine 2")

#/t - tab
print("Hello\tI\tam\tWint\tPhyusin\tMaung")

#/b - backspace
print("Helloo\b")

#\\ - \
print("lions\\tigers\\bears")

#\r - carriage return
print("Writing old school carriage text\rNew text")

print("A \" is one")

#\" - "
print("My name \"Wint\" hehe ")

#\' - '
print("A \'hi\' is another name for something.")

#'A' - 65 base 10

#\ooo - octal number
print("char: \101") #101 is base 10 number 65 this is the character uppercase a

#x\hh -> x followed by hexadecimal number
print("char: \x41") 

# \f - form feed
print("Page 1\fPage 2\fPage 3")